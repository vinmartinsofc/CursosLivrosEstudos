package devdojo.exfor;

public class For14 {

    public static void main(String[] args) {
        int rand = 5;
        int result = 0;
        int temp = 0;

        System.out.println("Number is: " + rand);

        for (int i = 5; i > 0; i--) {

            System.out.println(rand + " x " + i + " is " + result);

        }

        System.out.println("Temp is: " + temp);

    }

}
